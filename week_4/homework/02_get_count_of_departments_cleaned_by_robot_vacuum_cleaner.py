#       북  동  남 서
dr = [-1, 0, 1, 0]
dc = [0, 1, 0, -1]
# idx = 0  1  2  3
# 북쪽에서 왼쪽으로 회전하면? 서쪽  / idx는 0 => 3
# 동쪽에서 왼쪽으로 회전하면? 북쪽  / idx는 1 => 0
# 남쪽에서 왼쪽으로 회전하면? 동쪽  / idx는 2 => 1
# 서쪽에서 왼쪽으로 회전하면? 남쪽 / idx는 3 => 2
def get_d_index_when_rotate_to_left(d):
    return (d+3) % 4 #위의 네가지를 봤을 때 바뀌는 인덱스를 계산한 값

# 북쪽에서 뒤로 돌기? 남쪽 / idx는 0 => 2
# 동쪽에서 뒤로 돌기? 서쪽 / idx는 1 => 3
# 남쪽에서 뒤로 돌기? 북쪽 / idx는 2 => 0
# 서쪽에서 뒤로 돌기? 동쪽 / idx는 3 => 1
def get_d_index_when_go_back(d):
    return (d+2) % 4

def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    n = len(room_map)
    m = len(room_map[0])
    count_of_departments_cleaned = 1
    room_map[r][c] = 2
    queue = list([[r,c,d]])

    while queue:
        r, c, d = queue.pop(0)
        temp_d = d

        for i in range(4):
            temp_d = get_d_index_when_rotate_to_left(temp_d)
            new_r, new_c = r + dr[temp_d], c + dc[temp_d]

            if 0 <= new_r < n and 0 <= new_c < m and room_map[new_r][new_c] == 0: #갈 수 있는 곳인지 없는지 판별
                count_of_departments_cleaned += 1
                room_map[new_r][new_c] = 2
                queue.append([new_r, new_c, temp_d])
                break

            elif i == 3: #갈 곳이 없는 경우
                new_r, new_c = r + dr[get_d_index_when_go_back(temp_d)], c + dc[get_d_index_when_go_back(temp_d)]
                queue.append([new_r, new_c, d])

                if room_map[new_r][new_c] == 1: #뒤가 벽인 경우
                    return count_of_departments_cleaned

